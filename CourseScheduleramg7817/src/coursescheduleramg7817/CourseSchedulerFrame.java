    private void studentDropCourseDropButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentDropCourseDropButtonActionPerformed
        String courseCode = studentDropCourseComboBox.getSelectedItem().toString();

        // determine if student is scheduled or waitlisted for the course.
        ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(currentSemester, currentStudID);
       
        String status = "";
        
        for (ScheduleEntry entry : studentSchedule) {
            
            if (entry.getCourseCode().equals(courseCode)) {
               
                status = entry.getStatus();
                
                break;
            
            }
        
        }
        
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, currentStudID, courseCode);
        
        if (status.equals("s")) {
            
            studentDropCourseTextArea.setText(studentTabCurrentStudentLabel.getText() + " has been dropped from " + courseCode + "\n");

            // see if a student needs scheduled from the waitlist.
            
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode);
            
            if (schedule.size() > 0) {
                
                
                ScheduleQueries.updateScheduleEntry(schedule.get(0));
                
                String studentName = StudentQueries.getStudent(schedule.get(0).getStudentID()).toString();
                
                studentDropCourseTextArea.append(studentName + " has been scheduled into " + courseCode + "\n");
            
            }
            
        } else {
            
            studentDropCourseTextArea.setText(studentTabCurrentStudentLabel.getText() + " has been dropped from the waitlist for " + courseCode + "\n");
        
        }
        
        setStudentDropCourseComboBox();
    }//GEN-LAST:event_studentDropCourseDropButtonActionPerformed

