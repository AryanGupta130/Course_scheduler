    private void scheduleCoursesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesSubmitButtonActionPerformed
        String courseCode = scheduleCourseCourseComboBox.getSelectedItem().toString();

        // determine studentID from combobox of name.
        ArrayList<StudentEntry> students = StudentQueries.getStudentList();

        java.sql.Timestamp timestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        // Get number of seats the course will hold.
        int seatsPossible = CourseQueries.getCourseSeats(currentSemester, courseCode);

        // Get number of seats already reserved.
        int reservedSeats = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);

        // check to see if student can be scheduled or goes to wait status.
        if (reservedSeats < seatsPossible) {
            // add student as scheduled.
            
            ScheduleEntry student = new ScheduleEntry(currentSemester, courseCode, currentStudID, "s", timestamp);
            
            ScheduleQueries.addScheduleEntry(student);
            
            scheduleCourseStatusLabel.setText(currentStudentName + " has been scheduled for the class");
        } else {
            // add student as waitlisted.
            
            ScheduleEntry student = new ScheduleEntry(currentSemester, courseCode, currentStudID, "w", timestamp);
            
            ScheduleQueries.addScheduleEntry(student);
            
            scheduleCourseStatusLabel.setText(currentStudentName + " has been waitlisted for the class");

        }
        
        setStudentDropCourseComboBox();
    }//GEN-LAST:event_scheduleCoursesSubmitButtonActionPerformed

