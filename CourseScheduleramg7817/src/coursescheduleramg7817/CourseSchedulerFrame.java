    public void rebuildSemesterComboBoxes() {
        
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        
        changeSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        
        if (semesters.size() > 0) {
            
            currentSemesterLabel.setText(semesters.get(0));
            
            currentSemester = semesters.get(0);
        
        } else {
            
            currentSemesterLabel.setText("None, add a semester.");
            
            currentSemester = "None";
        }
        
        rebuildCourseComboBoxes();
    
    }

    public void rebuildCourseComboBoxes() {
        
        ArrayList<String> courses = CourseQueries.getAllCourseCodes(currentSemester);
        
        
        scheduleCourseCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        
        adminDisplayCourseListComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        
        adminDropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        
        setStudentDropCourseComboBox();
    
    }

    public void rebuildStudentComboBoxes() {
        
        ArrayList<StudentEntry> students = StudentQueries.getStudentList();
        
        ArrayList<String> studentNames = new ArrayList<>();
        
        for (StudentEntry student : students) {
            
            String name = student.getLastName() + "," + student.getFirstName() + " " + student.getStudentID();
            
            studentNames.add(name);
        
        }
        
        changeStudentComboBox2.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        
        changeStudentComboBox3.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        
        adminDropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        
        changeStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        
        changeStudentComboBox1.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        
        if (students.size() > 0) {
            
            studentTabCurrentStudentLabel1.setText(studentNames.get(0));
            
            studentTabCurrentStudentLabel2.setText(studentNames.get(0));
            
            studentTabCurrentStudentLabel3.setText(studentNames.get(0));
            
            studentTabCurrentStudentLabel.setText(studentNames.get(0));
            
            currentStudID = students.get(0).getStudentID();
            
            currentStudentName = studentNames.get(0);
        } else {
            
            currentStudID = "None";
            
            currentSemesterLabel.setText("None, add a student.");
            
        }

        setStudentDropCourseComboBox();
    }

    public void setStudentDropCourseComboBox() {
        
        ArrayList<ScheduleEntry> sch = ScheduleQueries.getScheduleByStudent(currentSemester, currentStudID);
        
        ArrayList<String> courses = new ArrayList<>();
        
        for (ScheduleEntry entry : sch) {
            
            courses.add(entry.getCourseCode());

        }
        
        studentDropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));

    }
