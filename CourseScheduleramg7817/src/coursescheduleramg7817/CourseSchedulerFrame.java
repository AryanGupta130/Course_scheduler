    private void adminDropCourseDropButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropCourseDropButtonActionPerformed
        // TODO add your handling code here:
        // get the course to be dropped for the semester.
        String courseCode = adminDropCourseComboBox.getSelectedItem().toString();

        // retrieve the list of students scheduled or waitlisted for the course.
        ArrayList<ScheduleEntry> scheduledStudents = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseCode);
        
        ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode);

        // drop the students for the class from the schedule.
        ScheduleQueries.dropScheduleByCourse(currentSemester, courseCode);

        // drop the course from the course table.
        CourseQueries.dropCourse(currentSemester, courseCode);

        // display the students that were scheduled and waitlisted who got dropped.
        adminsDropCourseStatusTextArea.setText("Scheduled students dropped from the course: \n");
        
        for (ScheduleEntry studentEntry : scheduledStudents) {
            // get the sts table entry.
            
            StudentEntry sts = StudentQueries.getStudent(studentEntry.getStudentID());
            
            String name = sts.getLastName() + "," + sts.getFirstName() + " " + sts.getStudentID();
            
            adminsDropCourseStatusTextArea.append(name + "\n");
        
        }
        
        adminsDropCourseStatusTextArea.append("\nWaitlisted students dropped from the course: \n");
        
        for (ScheduleEntry studentEntry : waitlistedStudents) {
            
            // get the student table entry.
            
            StudentEntry sts = StudentQueries.getStudent(studentEntry.getStudentID());
            
            String name = sts.getLastName() + "," + sts.getFirstName() + " " + sts.getStudentID();
            
            adminsDropCourseStatusTextArea.append(name + "\n");
        
        }
        
        rebuildCourseComboBoxes();
    }//GEN-LAST:event_adminDropCourseDropButtonActionPerformed

    private void adminDropStudentDropButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropStudentDropButtonActionPerformed
        // TODO add your handling code here:
        // determine studentID from combobox of name.
        
        ArrayList<StudentEntry> students = StudentQueries.getStudentList();
        
        String studentID = students.get(adminDropStudentComboBox.getSelectedIndex()).getStudentID();

        // drop the student from the list of students.
        
        StudentQueries.dropStudent(studentID);
        
        String studentName = adminDropStudentComboBox.getSelectedItem().toString();
        
        adminDropStudentTextarea.setText(studentName + " dropped from the list of students.\n");

        // get arraylist of semesters.
        
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        // loop through and process for each semester.
        
        for (String semester : semesters) {

            adminDropStudentTextarea.append("\nSemester: " + semester + "\n");
            // determine if student is scheduled or waitlisted for the course.
           
            ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            
            String status = "";
            
            for (ScheduleEntry entry : studentSchedule) {
               
                String courseCode = entry.getCourseCode();
                
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);
                
                status = entry.getStatus();
                
                if (status.equals("s")) {
                    
                    adminDropStudentTextarea.append(studentName + " dropped from " + courseCode + "\n");

                    // see if a student needs scheduled from the waitlist.
                   
                    ArrayList<ScheduleEntry> schedule = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode);
                    
                    if (schedule.size() > 0) {
                        //Change the new students status from waitlisted to scheduled.
                        
                        ScheduleQueries.updateScheduleEntry(schedule.get(0));
                        
                        StudentEntry waitlistedStudent = StudentQueries.getStudent(schedule.get(0).getStudentID());
                        
                        String waitlistedStudentName = waitlistedStudent.getLastName() + "," + waitlistedStudent.getFirstName() + " " + waitlistedStudent.getStudentID();
                        
                        adminDropStudentTextarea.append(waitlistedStudentName + " scheduled into " + courseCode + "\n");
                    }
                    
                } else {
                    
                    adminDropStudentTextarea.append(studentName + " dropped from the waitlist for " + courseCode + "\n");
                
                }
            }

        }
        
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_adminDropStudentDropButtonActionPerformed

